    void themTaiKhoan() {
        Connection conn = KetNoiSQL.getConnection();
        int row = 0;
        String sql1 = "insert into TaiKhoan (tenDangNhap, matKhau, email, quyen) values(?,?,?,?)";
        try {
            PreparedStatement ps1 = conn.prepareStatement(sql1);

            String password = txtMatKhau.getText().trim();
            ps1.setString(1, txtTenDangNhap.getText().trim());
            ps1.setString(2, password);
            ps1.setString(3, txtEmail.getText().trim());
            ps1.setString(4, "Võ Sinh");

            row = ps1.executeUpdate();
            if (row > 0) {
                JOptionPane.showMessageDialog(this, "Thêm Tài khoản thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Nhập mã chưa đúng");
            }
            conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
        void themVoSinh() {
            Connection conn = KetNoiSQL.getConnection();
            String sql = "insert into voSinh ( hoTen, ngaySinh, gioiTinh, diaChi, email, sdt, cap,trangThai)"
                    + "values(?,?,?,?,?,?,?,?)";
            int row = 0;


            try {
                PreparedStatement ps = conn.prepareStatement(sql);

//                ps.setString(1, txtMaSoSV.getText().trim());
                ps.setString(1, txtHoTen.getText().trim());
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String date = sdf.format(txtNgaySinh.getDate());
                ps.setString(2, date);
                if ("Nam".equals(txtGioiTinh.getSelectedItem().toString())) {
                    ps.setString(3, "Nam");
                } else if("Nữ".equals(txtGioiTinh.getSelectedItem().toString())){
                    ps.setString(3, "Nữ");
                }else{
                    ps.setString(3, "Khác");
                }
                ps.setString(4, txtDiaChi.getText().trim());

                ps.setString(5, txtEmail.getText().trim());
                ps.setString(6, txtSdt.getText().trim());
                ps.setString(7, "1");
                ps.setString(8, "1");
                 row = ps.executeUpdate();

            } catch (SQLException ex) {
                ex.printStackTrace();
            }
    }
