    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        String hoTen = txtHoTen.getText().trim();
        Date ngaySinh = txtNgaySinh.getDate();        
        String gioiTinh =txtGioiTinh.getSelectedItem().toString();
        String diaChi = txtDiaChi.getText().trim();
        String sdt = txtSdt.getText().trim();
        String email = txtEmail.getText().trim();
        String tenDangNhap = txtTenDangNhap.getText().trim();
        String matKhau = txtMatKhau.getText().trim();
        String xacNhanMatKhau = txtXacNhanMatKhau.getText().trim();

        // Ràng buộc điều kiện chi tiết
        String ycsdt = "^[0]{1}[0-9]{9}";
        String yctenDangNhap = "^[a-zA-Z0-9]+$";

        //Kiểm tra tài khoản, thông tin đã tồn tại hay chưa
//        boolean kiemtracccd = new SinhVienDAO().KiemTraCCCDSV(cccdsv);
        boolean kiemtraemailvs = new voSinhDAO().KiemTraEmail(email);
//        boolean kiemtrasdt = new SinhVienDAO().KiemTraSDTSV(sodienthoaisv);
        boolean kiemtratendn = new voSinhDAO().KiemTraTenDN(tenDangNhap);
        boolean kiemtraemailbcn = new banChuNhiemDAO().KiemTraEmail(email);
//        boolean kiemtraemailnhanvien = new QuanLyNhanVienDAO().KiemTraEmailNV(emailsv);
//        boolean kiemtrasdtnhanvien = new QuanLyNhanVienDAO().KiemTraSDTNV(sodienthoaisv);
        

        if (hoTen.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không để trống họ tên. Vui lòng điền thông tin đầy đủ!");
        } else if (diaChi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không để trống địa chỉ. Vui lòng điền thông tin đầy đủ!");
        } else if (sdt.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không để trống số điện thoại. Vui lòng điền thông tin đầy đủ!");
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không để trống email. Vui lòng điền thông tin đầy đủ!");
        } else if (tenDangNhap.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không để trống tên đăng nhập. Vui lòng điền thông tin đầy đủ!");
        } else if (matKhau.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không để trống mật khẩu. Vui lòng điền thông tin đầy đủ!");
        } else if (xacNhanMatKhau.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không để trống xác nhận mật khẩu. Vui lòng điền thông tin đầy đủ!");
        } else if (!sdt.matches(ycsdt)) {
            JOptionPane.showMessageDialog(this, "Định dạng số điện thoại không dúng. Ví dụ : 0827364857");
        } else if (!email.contains("@gmail.com")) {
            JOptionPane.showMessageDialog(this, "Định dạng gmail không đúng. Vui lòng nhập lại");
        } else if (!matKhau.equals(xacNhanMatKhau)) {
            JOptionPane.showMessageDialog(this, "Mật khẩu nhập lại không trùng với mật khẩu. Vui lòng nhập lại!");
        } else if (kiemtraemailvs || kiemtraemailbcn ) {
            JOptionPane.showMessageDialog(this, "Email này đã được đăng ký!");
        } else if (kiemtratendn) {
            JOptionPane.showMessageDialog(this, "Tên đăng nhập đã tồn tại! Vui lòng dùng tên khác");
        } else if (!tenDangNhap.matches(yctenDangNhap)) {
            JOptionPane.showMessageDialog(this, "Tên đăng nhập chỉ có số và chữ trong alphabet");
        } else
        {
            int test = JOptionPane.showConfirmDialog(null, "Bạn chắc có muốn đăng ký hay không !", "Thông báo đăng ký", JOptionPane.YES_NO_OPTION);
            if (test == JOptionPane.YES_OPTION) {
                
                themTaiKhoan();
                themVoSinh();
                resetForm();
                dangNhap dn = new dangNhap();
                dispose();
                dn.show();
            } else if (test == JOptionPane.NO_OPTION) {
                JOptionPane.showMessageDialog(this, "Bạn đã hủy đăng ký thành công");
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

